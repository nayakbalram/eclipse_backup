package ControlStatement;

public class test0 {

	public static void main(String[] args) {
		// When there is more than one condition then we can go for else-if condition
		// When all true condition is there then first true condition will get executed 
		if (true)
		{
			System.out.println("from if");
		}
		else if (true)
		{
			System.out.println("from if-1");
		}
		else if (true)
		{
			System.out.println("from if-2");
		}
		else
		{
			System.out.println("from else");
		}
		System.out.println("Execution done");

	}

}
